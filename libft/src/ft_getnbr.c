int		ft_getnbr(char *str)
{
	int		i;
	int		nbr;

	i = 0;
	nbr = 0;
	if (str[i] == '+' || str[i] == '-')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		nbr = (nbr * 10) + (str[i] - '0');
		i++;
	}
	if (str[0] == '-')
		nbr *= -1;
	return (nbr);
}
